//2. How to Access Item from Array in JavaScript.

/*
Accessing Array Elements
You access an array element by referring to the index number:[0,1,2,3]
indexing start from 0
*/

var employee=["prashanth","kiran","vashir","Abhishek",1,5,true];
console.log(employee[3]);


/*Access the Full Array
With JavaScript, the full array can be accessed by referring to the array name:
*/

var emp=["prashanth","kiran","vashir","Abhishek",1,5,true];
console.log(emp);